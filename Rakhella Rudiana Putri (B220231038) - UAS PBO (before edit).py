#cara pada nomor 5
class Makanan_dan_Minuman:
    def __init__(self, kandung=0):
        #Atribut private, tidak dapat diakses langsung dari luar kelas
        self.__kandung = kandung
#metode untuk menambah nilai kandungan
    def tambah_kandung(self, amount):
        if amount > 0:
            self.__kandung += amount
            return f"berhasil menambah {amount}. nilai kandungan makanan dan minuman sekarang: {self.__kandung}"
        else:
            return "jumlah nilai kandungan tidak valid!"
#metode untuk mengurangi nilai kandungan
    def kurangi_kandung(self, amount):
        if 0 < amount <= self.__kandung:
            self.__kandung -= amount
            return f"berhasil mengurangi {amount}. nilai kandungan makanan dan minuman sekarang: {self.__kandung}"
        else:
            return "jumlah nilai kandungan tidak valid atau melebihi jumlah yang tersedia!"
#metode untuk check nilai kandungan
    def cek_kandung(self):
        return f"nilai kandungan makanan dan minuman: {self.__kandung}"
#membuat objek dari kelas Makanan dan Minuman
makanan_dan_minuman1 = Makanan_dan_Minuman()
#menambah nilai kandungan
print(makanan_dan_minuman1.tambah_kandung(101)) #"berhasil menambah 101. nilai kandungan makanan dan minuman sekarang: 101
#mengecheck nilai kandungan
print(makanan_dan_minuman1.cek_kandung())       #"nilai kandungan makanan dan minuman: 101"
#mengurangi nilai kandungan
print(makanan_dan_minuman1.kurangi_kandung(50)) #"berhasil mengurangi 50. nilai kandungan makanan dan minuman sekarang: 51"

